package me.bruneli.main;

public class Calculadora {
	
	//Método de somar que retorna os dois numeros passados somados
	//Soma retorna float para lidar com numeros quebrados
	public double somar(double a, double b) {
		return a + b;
	}
	
	//Método de subtrair que retorna os dois numeros passados subtraidos
	//Subtração retorna float para lidar com numeros quebrados
	public double subtrair(double a, double b) {
		return a - b;
	}
	
	//Método de divisao que retorna os dois numeros passados divididos
	//Divisão retorna float para lidar com numeros quebrados
	public double dividir(double a, double b) {
		return a / b;
	}

	//Método de multiplicacao que retorna os dois numeros passados multiplicados
	//Multiplicação retorna float para lidar com numeros quebrados
	public double multiplicar(double a, double b) {
		return a * b;
	}

}	
